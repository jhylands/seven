from combinatorics import perm_unique

EXPECTED_OUTPUT = [['     *', '    * ', '   *  ', '  *   ', ' *    ', '*     '], ['    **', '   * *', '   ** ', '  *  *', '  * * ', '  **  ', ' *   *', ' *  * ', ' * *  ', ' **   ', '*    *', '*   * ', '*  *  ', '* *   ', '**    '], ['   ***', '  * **', '  ** *', '  *** ', ' *  **', ' * * *', ' * ** ', ' **  *', ' ** * ', ' ***  ', '*   **', '*  * *', '*  ** ', '* *  *', '* * * ', '* **  ', '**   *', '**  * ', '** *  ', '***   '], ['  ****', ' * ***', ' ** **', ' *** *', ' **** ', '*  ***', '* * **', '* ** *', '* *** ', '**  **', '** * *', '** ** ', '***  *', '*** * ', '****  '], [' *****', '* ****', '** ***', '*** **', '**** *', '***** '], ['******']]
def test_perm_unique():
    acc = []
    for i in range(1, 7):
        acc_internal = []
        for comb in sorted(perm_unique("*"*i + " "*(6-i))):
            acc_internal.append("".join(comb))
        acc.append(acc_internal)
    print(acc)
    assert acc == EXPECTED_OUTPUT

